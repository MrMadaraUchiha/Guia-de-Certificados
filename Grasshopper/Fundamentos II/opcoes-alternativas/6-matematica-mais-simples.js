// Neste desafio, você usará os operadores de incremento e decremento para atualizar rapidamente uma variável. O operador de incremento ++ adiciona 1 a uma variável, enquanto o operador de decremento -- subtrai 1. Por exemplo, count++ é igual a count = count + 1.

let x = 5;
x++;
print('x is ' + x);
x--;
print('x is now ' + x);