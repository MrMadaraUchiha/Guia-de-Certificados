// Neste desafio, você foi atualizado rapidamente variáveis usando os operadores atribuição de adição += e atribuição de subtração -=. A formiga guardou 40 sementes para o inverno, enquanto o gafanhoto não guardou nenhuma. A generosa formiga decide presentear o gafanhoto com 10 sementes.

let ant = 40;
let gift = 10;
let grasshopper = 0;
ant -= gift; // 40-10 = ant; gift n altera, mantem-se 10
grasshopper += gift; // 0+10 = grasshopper
print('The Ant has ' + ant + 'seeds, and the Grasshopper has ' + grasshopper + ' seeds');

